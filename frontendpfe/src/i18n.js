function getValueByPath(object, path) {
  return path.split(".").reduce((acc, key) => (acc ? acc[key] : undefined), object);
}

export function createTranslator(language) {
  return (path, params = {}) => {
    const selected = translations[language] || translations.fr;
    const fallback = translations.fr;
    let value = getValueByPath(selected, path) ?? getValueByPath(fallback, path) ?? path;

    if (typeof value !== "string") return value;

    Object.entries(params).forEach(([key, paramValue]) => {
      value = value.replace(new RegExp(`\\{${key}\\}`, "g"), String(paramValue));
    });

    return value;
  };
}
